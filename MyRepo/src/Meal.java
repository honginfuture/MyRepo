
public class Meal {
	public String restName;
	public String mType;
	public String mName;
	public String disPrice;
	public String mPrice;
	public String deal;

	public Meal(String restName, String mType, String mName, String disPrice, String mPrice, String deal) {
		this.restName = restName;
		this.mType = mType;
		this.mName = mName;
		this.disPrice = disPrice;
		this.mPrice = mPrice;
		this.deal = deal;
	}

}
